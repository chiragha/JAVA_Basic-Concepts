
package com.mycompany.oop.Overloaded_constructor;

public class Overloaded_constructor {
    public static void main(String[] args) {
        Recipe recipe = new Recipe("brown-bread", "hot-tea", "basmati");
        System.out.println(recipe.rice);
        System.out.println(recipe.bread);
        System.out.println(recipe.dosa);
        System.out.println(recipe.tea);
        System.out.println(recipe.tomato);
        
    }
}
